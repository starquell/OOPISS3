package com.example.lab3;

/// maybe record?
public class TimedCoordinate {
    public Float x;
    public Float y;
    public Float z;

    public Long ms;

    @Override
    public String toString() {
        return "Point{" +
                "x=" + x +
                ", y=" + y +
                ", z=" + z +
                ", ms=" + ms +
                '}';
    }
}
